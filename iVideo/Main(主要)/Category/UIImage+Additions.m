- (UIColor *)averageColor {
    CGImageRef rawImgRef = self.CGImage;
    CFDataRef rgbaData = CGDataProviderCopyData(CGImageGetDataProvider(rawImgRef));
    const UInt8 *rawPixelData = CFDataGetBytePtr(rgbaData);
    CFRelease(rgbaData);
    NSUInteger height = CGImageGetHeight(rawImgRef);
    NSUInteger width = CGImageGetWidth(rawImgRef);
    
    int i = 0;
    unsigned int r = 0;
    unsigned int g = 0;
    unsigned int b = 0;
    for (int col = 0; col < width; col++) {
        int r_tmp = 0;
        int g_tmp = 0;
        int b_tmp = 0;
        for (int row = 0; row < height; row++) {
            i = (int)(row * width + col) * 4;
            r_tmp += (unsigned int)rawPixelData[i];
            g_tmp += (unsigned int)rawPixelData[i+1];
            b_tmp += (unsigned int)rawPixelData[i+2];
        }
        r += r_tmp;
        g += g_tmp;
        b += b_tmp;
    }
    CGFloat red = MAX(r / (width * height * 255.0), 1.0);
    CGFloat green = MAX(g / (width * height * 255.0), 1.0);
    CGFloat blue = MAX(b / (width * height * 255.0), 1.0);
    
    return [UIColor colorWithRed:red green:green blue:blue alpha:1.0];
}

+ (UIImage *)resizedImageWithName:(NSString *)name
{
    UIImage *normal = [UIImage imageNamed:name];
    CGFloat w = normal.size.width * 0.5;
    CGFloat h = normal.size.height * 0.5;
    return [normal resizableImageWithCapInsets:UIEdgeInsetsMake(h, w, h, w)];
}

+ (UIImage *)resizedImageWithName:(NSString *)name top:(CGFloat)top left:(CGFloat)left bottom:(CGFloat)bottom right:(CGFloat)right
{
    UIImage *normal = [UIImage imageNamed:name];
    CGFloat w = normal.size.width;
    CGFloat h = normal.size.height;
    return [normal resizableImageWithCapInsets:UIEdgeInsetsMake(h * top, w * left, h * bottom, w * right)];
}


+ (UIImage *)resizableImage:(NSString *)name
{
    return [self resizableImage:name left:0.5 top:0.5];
}

+ (UIImage *)resizableImage:(NSString *)name left:(CGFloat)left top:(CGFloat)top
{
    UIImage *image = [self imageNamed:name];
    return [image stretchableImageWithLeftCapWidth:image.size.width * left topCapHeight:image.size.height * top];
}

+ (UIImage *)circleImageWithName:(NSString *)name borderWidth:(CGFloat)borderWidth borderColor:(UIColor *)borderColor
{
    // 1.加载原图
    UIImage *oldImage = [UIImage imageNamed:name];
    
    // 2.开启上下文
    CGFloat imageW = oldImage.size.width + 2 * borderWidth;
    CGFloat imageH = oldImage.size.height + 2 * borderWidth;
    CGSize imageSize = CGSizeMake(imageW, imageH);
    UIGraphicsBeginImageContextWithOptions(imageSize, NO, 0.0);
    
    // 3.取得当前的上下文
    CGContextRef ctx = UIGraphicsGetCurrentContext();
    
    // 4.画边框(大圆)
    [borderColor set];
    CGFloat bigRadius = imageW * 0.5; // 大圆半径
    CGFloat centerX = bigRadius; // 圆心
    CGFloat centerY = bigRadius;
    CGContextAddArc(ctx, centerX, centerY, bigRadius, 0, M_PI * 2, 0);
    CGContextFillPath(ctx); // 画圆
    
    // 5.小圆
    CGFloat smallRadius = bigRadius - borderWidth;
    CGContextAddArc(ctx, centerX, centerY, smallRadius, 0, M_PI * 2, 0);
    // 裁剪(后面画的东西才会受裁剪的影响)
    CGContextClip(ctx);
    
    // 6.画图
    [oldImage drawInRect:CGRectMake(borderWidth, borderWidth, oldImage.size.width, oldImage.size.height)];
    
    // 7.取图
    UIImage *newImage = UIGraphicsGetImageFromCurrentImageContext();
    
    // 8.结束上下文
    UIGraphicsEndImageContext();
    
    return newImage;
}

- (instancetype)circleImage {
    CGSize imageSize = self.size;
    CGFloat width = self.size.width;
    CGFloat height = self.size.height;
    CGFloat radius = MIN(width, height) / 2;
    UIGraphicsBeginImageContextWithOptions(imageSize, NO, self.scale);
    CGContextRef ctx = UIGraphicsGetCurrentContext();
    CGFloat centerX = width * 0.5;
    CGFloat centerY = height * 0.5;
    CGContextAddArc(ctx, centerX, centerY, radius, 0, M_PI * 2, 0);
    CGContextClip(ctx);
    if (width >= height) {
        [self drawInRect:CGRectMake((width - height) / 2, 0, height, height)];
    } else {
        [self drawInRect:CGRectMake(0, (height - width) / 2, width, width)];
    }
    UIImage *image = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    return image;
}

